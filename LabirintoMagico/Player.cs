class Player
{
    int playerPosX;
    int playerPosY;

    public int GetPlayerPosX()
    {
        return playerPosX;
    }
    public int GetPlayerPosY()
    {
        return playerPosY;
    }

    public void SetPlayerPosX(int posizioneX)
    {
        playerPosX = posizioneX;
    }
    public void SetPlayerPosY(int posizioneY)
    {
        playerPosY = posizioneY;
    }
}